#include "CommandLineOptions.hpp"

namespace Mo {

namespace {

////////////////////////////////////////////////////////////////////////////////
// Local Functions
////////////////////////////////////////////////////////////////////////////////
//! @brief Constructs a definition of the command line argument schema.
Ag::Cli::Schema createSchema()
{
    Ag::Cli::SchemaBuilder builder;

    builder.setName("Mighty Oak");
    builder.setDescription("An Archimedes and RiscPC emulator.");

    return builder.createSchema();
}

} // Anonymous namespace

////////////////////////////////////////////////////////////////////////////////
// CommandLineOptions Member Function Definitions
////////////////////////////////////////////////////////////////////////////////
CommandLineOptions::CommandLineOptions() :
    Ag::Cli::ProgramArguments(createSchema())
{
}

} // namespace Mo
